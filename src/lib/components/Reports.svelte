<script lang="ts">
	import pdf from 'pdfjs';
	export let tableExport: any;
	export let columnNames: any;

    
	async function getReport(tableExport , columnNames , type) {
		
		try {
			const requestData = {
				columnNames: columnNames,
				tableExport: tableExport,
				type: type
			};

			const response = await fetch('/api/excelTable', {
				method: 'POST', // Assuming you want to send data through a POST request
				headers: {
					'Content-Type': 'application/json',
					Accept: 'application/pdf'
				},
				body: JSON.stringify(requestData)
			});

			if (!response.ok) {
				throw new Error('Request failed');
			}

			const blob = await response.blob(); // Get the response as a Blob
			const pdfUrl = URL.createObjectURL(blob); // Create a URL for the Blob

			// Example: Open the PDF in a new tab
			window.open(pdfUrl, '_blank');

			// If you want to download the PDF, you can create a link and trigger a click event
			// const link = document.createElement('a');
			// link.href = pdfUrl;
			// link.download = 'file.pdf';
			// link.click();
		} catch (error) {
			console.error('Error fetching PDF:', error);
		}
	}
